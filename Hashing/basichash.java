package Hashing;
import java.util.*;

public class basichash {
    public static void main(String[] args) {
        HashSet<Integer> hs=new HashSet<>();
        hs.add(1);
        hs.add(2);
        hs.add(3);
        hs.add(4);
        System.out.println("Hash size : "+hs.size());
        System.out.println("Elements in Hash set : "+hs);
    }
}
